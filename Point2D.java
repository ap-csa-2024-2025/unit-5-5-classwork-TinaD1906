public class Point2D
{
    private int x;
    private int y;

    //Accessors methods (getters)
    public int getX()
    {
        return x;
    }
    public int getY()
    {
        return y;
    }

    //mutator methods (setters)
    public void setX(int input)
    {
        x = input;
    }

    public void setY(int input)
    {
        y = input;
    }


}
